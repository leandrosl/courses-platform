import { db } from '../db/client.js'

function normalizeCourse(row) {
  if (!row) {
    return null
  }

  return {
    id: Number(row.id),
    title: row.title,
    description: row.description,
    workloadHours: Number(row.workloadHours),
    createdAtUtc:
      row.createdAtUtc instanceof Date
        ? row.createdAtUtc.toISOString()
        : new Date(row.createdAtUtc).toISOString(),
  }
}

export async function ensureCoursesTable() {
  await db`
    CREATE TABLE IF NOT EXISTS courses (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      title VARCHAR(200) NOT NULL,
      description VARCHAR(1000),
      workload_hours INTEGER NOT NULL CHECK (workload_hours > 0),
      created_at_utc TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `
}

export async function listCourses() {
  const rows = await db`
    SELECT
      id,
      title,
      description,
      workload_hours AS "workloadHours",
      created_at_utc AS "createdAtUtc"
    FROM courses
    ORDER BY id ASC
  `

  return rows.map(normalizeCourse)
}

export async function getCourseById(id) {
  const rows = await db`
    SELECT
      id,
      title,
      description,
      workload_hours AS "workloadHours",
      created_at_utc AS "createdAtUtc"
    FROM courses
    WHERE id = ${id}
  `

  return normalizeCourse(rows[0] ?? null)
}

export async function createCourse(input) {
  const rows = await db`
    INSERT INTO courses (title, description, workload_hours, created_at_utc)
    VALUES (${input.title}, ${input.description}, ${input.workloadHours}, NOW())
    RETURNING
      id,
      title,
      description,
      workload_hours AS "workloadHours",
      created_at_utc AS "createdAtUtc"
  `

  return normalizeCourse(rows[0])
}

export async function updateCourse(id, input) {
  const rows = await db`
    UPDATE courses
    SET
      title = ${input.title},
      description = ${input.description},
      workload_hours = ${input.workloadHours}
    WHERE id = ${id}
    RETURNING
      id,
      title,
      description,
      workload_hours AS "workloadHours",
      created_at_utc AS "createdAtUtc"
  `

  return normalizeCourse(rows[0] ?? null)
}

export async function deleteCourse(id) {
  const rows = await db`
    DELETE FROM courses
    WHERE id = ${id}
    RETURNING id
  `

  return rows.length > 0
}
